// Funkcja do wyszukiwania odpadów
function searchOdpad(query) {
    if (!query || query.trim() === '') {
        return [];
    }
    
    const normalizedQuery = query.toLowerCase().trim();
    return odpady.filter(odpad => {
        const nazwa = odpad.nazwa.toLowerCase();
        return nazwa.includes(normalizedQuery);
    });
}

// Funkcja do wyświetlania wyników
function displayResults(results) {
    const container = document.getElementById('resultContainer');
    container.innerHTML = '';
    
    if (results.length === 0) {
        container.innerHTML = `
            <div class="no-result">
                Nie znaleziono takiego odpadu
            </div>
        `;
        return;
    }
    
    results.forEach(odpad => {
        const card = document.createElement('div');
        card.className = `result-card ${odpad.kolor}`;
        
        card.innerHTML = `
            <div class="result-header">
                <div class="kosz-badge ${odpad.kolor}"></div>
                <div class="result-title">
                    <h2>${odpad.nazwa}</h2>
                    <div class="kosz-name">${odpad.kosz}</div>
                </div>
            </div>
            ${odpad.opis ? `<div class="result-description">${odpad.opis}</div>` : ''}
        `;
        
        container.appendChild(card);
    });
}

// Event listeners
document.addEventListener('DOMContentLoaded', function() {
    const searchInput = document.getElementById('searchInput');
    const searchButton = document.getElementById('searchButton');
    
    function performSearch() {
        const query = searchInput.value;
        const results = searchOdpad(query);
        displayResults(results);
    }
    
    searchButton.addEventListener('click', performSearch);
    
    searchInput.addEventListener('keypress', function(e) {
        if (e.key === 'Enter') {
            performSearch();
        }
    });
});
